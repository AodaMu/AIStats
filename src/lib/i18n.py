def get_lang():
    """获取当前语言设置"""
    if 'language' not in st.session_state:
        st.session_state.language = 'zh'  # 默认中文
    return st.session_state.language

def set_lang(lang):
    """设置语言"""
    st.session_state.language = lang

def t(key):
    """获取翻译文本
    
    Args:
        key: 文本键名
    
    Returns:
        翻译后的文本，如果找不到返回键名
    """
    lang = get_lang()
    if key in TEXTS:
        return TEXTS[key].get(lang, TEXTS[key].get('zh', key))
    return key

def render_language_switcher():
    """渲染语言切换按钮"""
    lang = get_lang()
    
    col1, col2 = st.columns(2)
    with col1:
        if st.button("🇨🇳 中文", use_container_width=True, 
                     type="primary" if lang == 'zh' else "secondary"):
            set_lang('zh')
            st.rerun()
    with col2:
        if st.button("🇲🇳 Монгол", use_container_width=True,
                     type="primary" if lang == 'mn' else "secondary"):
            set_lang('mn')
            st.rerun()
